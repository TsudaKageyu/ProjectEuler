    int32_t EvaluateNumber(char c)
    {
        // Convert '2'...'A' to 2...14

        switch (c)
        {
        case 'A':
            return 14;
        case 'K':
            return 13;
        case 'Q':
            return 12;
        case 'J':
            return 11;
        case 'T':
            return 10;
        default:
            assert('2' <= c && c <= '9');
            return static_cast<int32_t>(c) - '0';
        }
    }

    int32_t EvaluateHand(const char *hand)
    {
        // Extract the numbers.

        array<int32_t, 5> nums;
        for (size_t i = 0; i < nums.size(); ++i)
            nums[i] = EvaluateNumber(hand[i * 2]);

        sort(nums.begin(), nums.end());

        // Check for Straight.

        const bool straight
            =  (nums[1] == nums[0] + 1 || (nums[1] == 2 && nums[0] == 14))
            && (nums[2] == nums[0] + 2)
            && (nums[3] == nums[0] + 3)
            && (nums[4] == nums[0] + 4);

        // Check for Flash.

        const bool flash
            =  (hand[0] == hand[1])
            && (hand[0] == hand[2])
            && (hand[0] == hand[3])
            && (hand[0] == hand[4]);

        // Weigh the hand.

        if (straight && flash)
        {
            // Royal Flush or Straight Flush

            if (nums[0] == 10)
                return 9 << 16;
            else
                return (8<< 16) + nums[4];
        }

        // Flash.

        if (flash)
            return (5 << 16) + nums[4];

        // Straight.

        if (straight)
            return (4 << 16) + nums[4];

        // Check for the set of cards.

        vector<int> four;
        vector<int> three;
        vector<int> two;
        vector<int> one;

        for (auto it1 = nums.rbegin(); it1 != nums.rend();)
        {
            int count = 1;
            for (auto it2 = it1 + 1; it2 != nums.rend(); ++it2)
            {
                if (*it2 != *it1)
                    break;

                count++;
            }

            if (count == 4)
                four.push_back(*it1);
            else if (count == 3)
                three.push_back(*it1);
            else if (count == 2)
                two.push_back(*it1);
            else if (count == 1)
                one.push_back(*it1);

            it1 += count;
        }

        // Four of a Kind.

        if (!four.empty())
            return (7 << 16) + (four[0] << 8) + one[0];

        // Full House.

        if (!three.empty() && !two.empty())
            return (6 << 16) + (three[0] << 8) + two[0];

        // Three of a Kind.

        if (!three.empty())
            return (3 << 16) + (three[0] << 8) + one[0];

        // Two Pairs.

        if (two.size() == 2)
            return (2 << 16) + (two[0] << 12) + (two[1] << 8) + one[0];

        // One Pair.

        if (two.size() == 1)
            return (1 << 16) + (two[0] << 8) + one[0];

        // High Card.

        return one[0];
    }
